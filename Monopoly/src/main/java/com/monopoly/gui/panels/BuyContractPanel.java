    private void HandleConfermaButton() {
        for (Contratto contratto : contrattiDisponibili) {
            if (contrattiList.getSelectedValue().contains(contratto.getNome())){
                
                giocatoreAttuale.addContrattiPosseduti(contratto);
                contratto.setProprietario(giocatoreAttuale);

                denaroBeforeAcquisto = giocatoreAttuale.getDenaro();
                giocatoreAttuale.addDenaro(-contratto.getPrezzoVendita());
                
                contratto.setDisponibile(false);
                
                JOptionPane.showMessageDialog(
                        this.getParent(), 
                        "Contratto acquistato con successo.",
                        "Acquisto effettuato",
                        JOptionPane.INFORMATION_MESSAGE
                );
                
                this.gameboardPanel.updateInfoTextArea();
                this.actualFrame.dispose();
            }
        }
    }
